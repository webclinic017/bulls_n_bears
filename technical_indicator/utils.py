def get_curr_month_last_thursday():
        days_curr_month = monthrange(datetime.datetime.today().year, datetime.datetime.today().month)
        today = datetime.datetime.today().astimezone(pytz.timezone('Asia/Kolkata'))
        this_thursday = today + relativedelta(weekday=calendar.THURSDAY)
        next_thursday = this_thursday + datetime.timedelta(days=7)
        while(next_thursday.day <= days_curr_month[1]-7):
                next_thursday = next_thursday + datetime.timedelta(days=7)
        next_thursday = next_thursday.strftime('%d-%b-%Y')
        return next_thursday


